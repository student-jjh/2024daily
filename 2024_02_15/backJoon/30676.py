N = int(input())

if 380 <= N < 425:
    print("Violet")

elif 425 <= N <450:
    print("Indigo")

elif 450<= N < 495:
    print("Blue")

elif 495 <= N < 570:
    print("Green")

elif 570 <= N < 590:
    print("Yellow")

elif 590 <= N < 620:
    print("Orange")

else:
    print("Red")